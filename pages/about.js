export async function getStaticProps() {
    const {data : markdownData} = await axios.get('https://raw.githubusercontent.com/kenryuS/kenryuS/main/README.md');

    return {
        props: {
            mdData : markdownData.replace(/\n/g, "<br>")
        }
    }
}
